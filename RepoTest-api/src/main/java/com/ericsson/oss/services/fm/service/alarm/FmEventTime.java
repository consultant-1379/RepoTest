package com.ericsson.oss.services.fm.service.alarm;

import java.io.Serializable;
import java.util.Date;

import com.ericsson.oss.itpf.sdk.eventbus.annotation.PersistenceType;
import com.ericsson.oss.itpf.sdk.modeling.eventbus.annotation.ModeledAttribute;
import com.ericsson.oss.itpf.sdk.modeling.eventbus.annotation.ModeledEventDefinition;

/**
 * @author tcsjapa
 *
 */
@ModeledEventDefinition(persistenceType = PersistenceType.NON_PERSISTENT, defaultChannelId = "FMMediationChannel", description = "FmEventTime", version = "1.0.0")
public class FmEventTime implements Serializable {

	@ModeledAttribute(description = "")
	private Date theTime;
	@ModeledAttribute(description = "")
	private String timeZone;

	/**
	 * @return the theTime
	 */
	public Date getTheTime() {
		return theTime;
	}

	/**
	 * @param theTime
	 *            the theTime to set
	 */
	public void setTheTime(final Date theTime) {
		this.theTime = theTime;
	}

	/**
	 * @return the timeZone
	 */
	public String getTimeZone() {
		return timeZone;
	}

	/**
	 * @param timeZone
	 *            the timeZone to set
	 */
	public void setTimeZone(final String timeZone) {
		this.timeZone = timeZone;
	}

	/**
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "FmScheduledEventTime [theTime=" + theTime + ", timeZone="
				+ timeZone + "]";
	}

}
